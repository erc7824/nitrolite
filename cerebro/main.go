package main

import (
	"fmt"
	"log"
	"os"
	"os/exec"
	"path/filepath"

	"github.com/c-bata/go-prompt"
	"golang.org/x/term"
)

func main() {
	log.SetFlags(0)
	log.SetPrefix("clearnode-cli: ")
	log.SetOutput(os.Stderr)
	if len(os.Args) < 2 {
		_, _ = fmt.Fprintf(os.Stderr, "Usage: clearnode-cli <clearnode_ws_url>\n")
		_, _ = fmt.Fprintf(os.Stderr, "Example: clearnode-cli wss://clearnode.example.com/ws\n")
		log.Fatalln("invalid arguments")
	}

	wsURL := os.Args[1]

	// Get config directory
	configDir := os.Getenv("CLEARNODE_CLI_CONFIG_DIR")
	if configDir == "" {
		userConfDir, err := os.UserConfigDir()
		if err != nil {
			log.Fatalf("failed to get user config directory: %v", err)
		}
		configDir = filepath.Join(userConfDir, "clearnode-cli")
	}

	if err := os.MkdirAll(configDir, 0755); err != nil {
		log.Fatalf("failed to create config directory: %v", err)
	}

	// Initialize storage
	storagePath := filepath.Join(configDir, "config.db")
	store, err := NewStorage(storagePath)
	if err != nil {
		log.Fatalf("failed to initialize storage: %v", err)
	}

	// Create operator
	operator, err := NewOperator(wsURL, store)
	if err != nil {
		log.Fatalf("failed to create operator: %v", err)
	}

	fmt.Println("Clearnode CLI - SDK Development Tool")
	fmt.Printf("Connected to: %s\n", wsURL)
	fmt.Printf("Config directory: %s\n", configDir)
	fmt.Println("\nType 'help' for available commands or 'exit' to quit")

	// Terminal handling
	initialState, _ := term.GetState(int(os.Stdin.Fd()))
	handleExit := func() {
		term.Restore(int(os.Stdin.Fd()), initialState)
		exec.Command("stty", "sane").Run()
	}

	options := append(getStyleOptions(),
		prompt.OptionPrefix("clearnode> "),
		prompt.OptionAddKeyBind(prompt.KeyBind{
			Key: prompt.ControlC,
			Fn: func(buf *prompt.Buffer) {
				fmt.Println("\nExiting Clearnode CLI")
				handleExit()
				os.Exit(0)
			},
		}),
		prompt.OptionAddKeyBind(prompt.KeyBind{
			Key: prompt.ControlD,
			Fn:  func(buf *prompt.Buffer) {},
		}),
	)

	p := prompt.New(
		operator.Execute,
		operator.Complete,
		options...,
	)

	promptExitCh := make(chan struct{})
	go func() {
		p.Run()
		close(promptExitCh)
	}()

	select {
	case <-operator.Wait():
		log.Println("connection closed.")
	case <-promptExitCh:
		log.Println("session ended.")
	}

	handleExit()
	log.Println("exiting...")
}

func getStyleOptions() []prompt.Option {
	return []prompt.Option{
		prompt.OptionTitle("Clearnode CLI"),
		prompt.OptionPrefixTextColor(prompt.Green),
		prompt.OptionPreviewSuggestionTextColor(prompt.Blue),

		prompt.OptionSuggestionTextColor(prompt.White),
		prompt.OptionSuggestionBGColor(prompt.DarkGray),

		prompt.OptionDescriptionTextColor(prompt.Black),
		prompt.OptionDescriptionBGColor(prompt.Cyan),

		prompt.OptionSelectedSuggestionTextColor(prompt.Black),
		prompt.OptionSelectedSuggestionBGColor(prompt.Green),

		prompt.OptionSelectedDescriptionTextColor(prompt.White),
		prompt.OptionSelectedDescriptionBGColor(prompt.DarkBlue),

		prompt.OptionShowCompletionAtStart(),
	}
}
